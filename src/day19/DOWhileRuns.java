package day19;

public class DOWhileRuns {
public static void main(String[] args) {
	
		int count = 10;
		do {
			System.out.println("Count:" + count);
			count++;
		} while (count <= 5);
	
	// in this example, do while loop body code will run 
// once then test the condition , then exit the loop, as condition is false.
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}

}